package Lab18_07;

public class DataType {

    public static void main(String[] args) {
      
        String text = "Hello";
        int number = 42;
        float decimal = 3.14f;

       
        System.out.println("Value: " + text + " | Data Type: String");
        System.out.println("Value: " + number + " | Data Type: int");
        System.out.println("Value: " + decimal + " | Data Type: float");
    }
}
